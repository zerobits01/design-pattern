# instead on large interface separate if
class Printer:
    @abstractmethod
    def print(self, document): pass


class Scanner:
    @abstractmethod
    def scan(self, document): pass


# same for Fax, etc.

class MyPrinter(Printer):
    def print(self, document):
        print(document)


class Photocopier(Printer, Scanner):
    def print(self, document):
        print(document)

    def scan(self, document):
        pass  # something meaningful

# if we seperate then combination is easier
# but if we combine from the base seperation is not possible
class MultiFunctionDevice(Printer, Scanner):  # , Fax, etc
    @abstractmethod # instead our definition
    def print(self, document):
        pass

    @abstractmethod
    def scan(self, document):
        pass


# Imp: try to seperate duties as well
# this handles stock problem 

class MultiFunctionMachine(MultiFunctionDevice):
    def __init__(self, printer, scanner):
        self.printer = printer
        self.scanner = scanner

    def print(self, document):
        self.printer.print(document)

    def scan(self, document):
        self.scanner.scan(document)


